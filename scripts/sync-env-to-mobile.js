/**
 * Reads root .env and writes API_URL + optional keys into both mobile apps' config.
 * Run from repo root: npm run sync-env
 */
const fs = require('fs');
const path = require('path');

const rootDir = path.join(__dirname, '..');
const envPath = path.join(rootDir, '.env');

function getEnvValue(content, key) {
  const match = content.match(new RegExp(`^${key}=(.+)$`, 'm'));
  return match ? match[1].trim() : '';
}

if (!fs.existsSync(envPath)) {
  console.warn('No .env found at project root. Create one from .env.example and run again.');
  process.exit(1);
}

const envContent = fs.readFileSync(envPath, 'utf8');
const apiUrl = getEnvValue(envContent, 'API_URL') || 'http://localhost:5000';
const googleMaps = getEnvValue(envContent, 'GOOGLE_MAPS_API_KEY') || '';
const firebaseSenderId = getEnvValue(envContent, 'VITE_FIREBASE_MESSAGING_SENDER_ID') || '';
const googleAndroidClientId = getEnvValue(envContent, 'EXPO_GOOGLE_ANDROID_CLIENT_ID') || '';
const googleWebClientId = getEnvValue(envContent, 'GOOGLE_CLIENT_ID') || '';
const oauthRedirectHttpsBase = getEnvValue(envContent, 'OAUTH_REDIRECT_HTTPS_BASE') || '';

const mobileEnv = `EXPO_PUBLIC_BASE_URL=${apiUrl}
EXPO_PUBLIC_GOOGLE_MAPS_API_KEY=${googleMaps}
EXPO_PUBLIC_FIREBASE_MESSAGING_SENDER_ID=${firebaseSenderId}
EXPO_PUBLIC_GOOGLE_ANDROID_CLIENT_ID=${googleAndroidClientId}
EXPO_PUBLIC_GOOGLE_WEB_CLIENT_ID=${googleWebClientId}
EXPO_PUBLIC_OAUTH_REDIRECT_HTTPS_BASE=${oauthRedirectHttpsBase}
`;

const configJs = `// Auto-generated by scripts/sync-env-to-mobile.js — do not edit by hand.
// Edit root .env and run: npm run sync-env

// Fallbacks so the app never crashes from undefined env
export const BASE_URL = process.env.EXPO_PUBLIC_BASE_URL || '';
export const GOOGLE_MAPS_API_KEY = process.env.EXPO_PUBLIC_GOOGLE_MAPS_API_KEY || '';
export const FIREBASE_MESSAGING_SENDER_ID = process.env.EXPO_PUBLIC_FIREBASE_MESSAGING_SENDER_ID || '';
export const GOOGLE_ANDROID_CLIENT_ID = process.env.EXPO_PUBLIC_GOOGLE_ANDROID_CLIENT_ID || '';
export const GOOGLE_WEB_CLIENT_ID = process.env.EXPO_PUBLIC_GOOGLE_WEB_CLIENT_ID || '';
export const OAUTH_REDIRECT_HTTPS_BASE = process.env.EXPO_PUBLIC_OAUTH_REDIRECT_HTTPS_BASE || '';
`;

const victimConfig = path.join(rootDir, 'mobile-victim', 'src', 'config', 'config.js');
const victimEnv = path.join(rootDir, 'mobile-victim', '.env');
const guardianConfig = path.join(rootDir, 'mobile-guardian', 'src', 'config', 'config.js');
const guardianEnv = path.join(rootDir, 'mobile-guardian', '.env');

[[victimConfig, victimEnv], [guardianConfig, guardianEnv]].forEach(([configFile, envFile]) => {
  const dir = path.dirname(configFile);
  if (!fs.existsSync(dir)) {
    console.warn('Skip (dir missing):', configFile);
    return;
  }
  fs.writeFileSync(configFile, configJs);
  fs.writeFileSync(envFile, mobileEnv);
  console.log('Updated:', path.relative(rootDir, configFile));
  console.log('Written:', path.relative(rootDir, envFile));
});

console.log('Done. BASE_URL =', apiUrl);
if (firebaseSenderId) console.log('      FIREBASE_MESSAGING_SENDER_ID set');
if (googleMaps) console.log('      GOOGLE_MAPS_API_KEY set');
if (googleAndroidClientId) console.log('      EXPO_GOOGLE_ANDROID_CLIENT_ID set');
if (oauthRedirectHttpsBase) console.log('      OAUTH_REDIRECT_HTTPS_BASE set (production Google redirect)');
